import UIKit

class DetailView: UIView {

    lazy var image: UIImage = {
        let image = UIImage()
        return image
    }()
    lazy var label: UILabel = {
        let label = UILabel()
        return label
    }()
    lazy var textView: UITextView = {
        let textView = UITextView()
        return textView
    }()

}
